#include<iostream>
using namespace std ;
int main()
{
	int first , second ,sum ,division,modulus , minus ;
	
	first = 10 ;
	second = 6 ;
	sum = first + second  ;
	cout<<"The sum is = "<<sum ;
	
	minus = first - second ;	
	cout<<"\nThe subtraction = "<<minus ;
	
	cout<<"\nThe product is = "<<first * second ;
	
	division = first / second ;
	cout<<"\nThe division is = "<<division ;
	
	modulus = first % second ;
	cout<<"\nThe modulus = "<<modulus ;
	 
	
	return 0 ;	 
}
